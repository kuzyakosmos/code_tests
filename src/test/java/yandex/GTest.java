package yandex;

import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.Test;

public class GTest {

    private final G g = new G();

    @Test
    void compareTest() {
        Node node1 = new Node(0);
        node1.distance = -1;
        Node node2 = new Node(1);
        node2.distance = 1;
        Assertions.assertEquals(node1.compareTo(node2), 1);
        node2.distance = 0;
        Assertions.assertEquals(node1.compareTo(node2), 1);


        node1.distance = -1;
        node2.distance = -2;
        Assertions.assertEquals(node1.compareTo(node2), 0);

        node1.distance = 1;
        node2.distance = 2;
        Assertions.assertEquals(node1.compareTo(node2), -1);

    }
}
